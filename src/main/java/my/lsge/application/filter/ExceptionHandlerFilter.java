package my.lsge.application.filter;

import com.fasterxml.jackson.core.JsonProcessingException;
import com.fasterxml.jackson.databind.ObjectMapper;
import io.jsonwebtoken.ExpiredJwtException;
import lombok.extern.slf4j.Slf4j;
import org.apache.commons.lang3.exception.ExceptionUtils;
import org.springframework.http.HttpStatus;
import org.springframework.http.ResponseEntity;
import org.springframework.security.core.AuthenticationException;
import org.springframework.web.filter.OncePerRequestFilter;

import javax.servlet.FilterChain;
import javax.servlet.ServletException;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import java.io.IOException;

@Slf4j
public class ExceptionHandlerFilter extends OncePerRequestFilter {

    @Override
    protected void doFilterInternal(HttpServletRequest request, HttpServletResponse response, FilterChain filterChain) throws ServletException, IOException {
        try {
            filterChain.doFilter(request, response);
        } catch (Exception e) {
            if (e instanceof ExpiredJwtException || e instanceof AuthenticationException) {
                response.setStatus(HttpStatus.UNAUTHORIZED.value());
                response.getWriter().write(convertObjectToJson(new ResponseEntity<>(e.getMessage(), HttpStatus.UNAUTHORIZED)));
            } else {
                response.setStatus(HttpStatus.BAD_REQUEST.value());
                response.getWriter().write(convertObjectToJson(new ResponseEntity<>(e.getMessage(), HttpStatus.BAD_REQUEST)));
                log.info(ExceptionUtils.getStackTrace(e));
            }
        }
    }

    private String convertObjectToJson(Object object) throws JsonProcessingException {
        if (object == null) {
            return null;
        }
        ObjectMapper mapper = new ObjectMapper();
        return mapper.writeValueAsString(object);
    }
}
